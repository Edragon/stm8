/*
================================================================================
Function 	:   IWDG_Init()
Description	:	IWDG Init
================================================================================
*/ 
static void IWDG_Init(void) 
{ 
    IWDG->KR = 0xCC;    //  start IWDG  
    IWDG->KR = 0x55;    //  key word
    IWDG->RLR = 0xff;
    IWDG->PR = 0x06;    //  64 --- 1.02s
    IWDG->KR = 0xAA;    //  refresh
}


/*
================================================================================
Function 	:   IWDG_fresh()
Description	:	IWDG fresh
================================================================================
*/ 
static void IWDG_fresh(void) 
{ 
    IWDG->KR = 0xAA;    //  refresh
}


