{
	char i;
	uint8 ii;
	uint8 dat[6];
	for(i = 0; i < 6; i++) dat[i] = 0; i = 0;	//初始化数据
	while(num / 10)								//拆位
	{
		dat[i] = num % 10;						//最低位
		num /= 10; i++;		
	}
	dat[i] = num;								//最高位
	ii = i;										//保存dat的位数
	for(; i >= 0; i--)	dat[i] += 48;			//转化成ASCII
	for(i = 0; i < num_bit; i++)
	LCD_printc6(x, y + i, ' ',word_color,back_color);		//清显示区域
	for(i = ii; i >= 0; i--)
	LCD_printc6(x++, y, dat[i],word_color,back_color);		//输出数值
}
